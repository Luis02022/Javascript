let num = 1


switch (num) {
    case 1:
        console.log("Domingo é o dia 1")
        break;

    case 2:
        console.log("Segunda-feira é o dia 2")
        break;
        
    case 3:
        console.log("Terça-feira é o dia 3")
        break;

    case 4:
        console.log("Quarta-feira é o dia 4")
        break;

    case 5:
        console.log("Quinta-feira é o dia 5")
        break;
            
    case 6: 
        console.log("Sexta-feira é o dia 6")
        break;

    case 7:
        console.log("Sábado é o dia 7")
        break;
            
    default:
        break;
}