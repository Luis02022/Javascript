// Criando função 
function somar(a,b){
    return a + b
}


function subtrair(a,b){
    return a - b
}

function multiplicar(a,b){
    return a * b
}

function dividir(a,b){
    return a/b
}

// Chamado a função 
const soma = somar(2,3)
const subtracao = subtrair(3,6)
const multipilcacao = multiplicar(6,9)
const divisao = dividir(4,9)

//Exibindo 
console.log(`Soma = ${soma}`)
console.log(`Soma = ${subtracao}`)
console.log(`Soma = ${multipilcacao}`)
console.log(`Soma = ${divisao}`)